pub fn display_to_char(code: &str) -> char {
    match code {
        "####### #  ## #  ##    #" => 'E',
        " #### #    ##  # # # ###" => 'G',
        "######  #     #   ######" => 'H',
        "######  #    # ## #    #" => 'K',
        "#######  #  #  ##  ##  #" => 'R',
        "#####      #     ###### " => 'U',
        "#   ###  # ## #  ###   #" => 'Z',
        &_ => '?',
    }
}